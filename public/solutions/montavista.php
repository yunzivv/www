    <script>
        (() => {
            const root = document.querySelector('[data-mv-tabs]');
            if (!root) return;

            const stage = root.querySelector('.mv-customer-stage');
            const img = root.querySelector('[data-stage]');
            const tabs = root.querySelectorAll('.mv-tab');
            const prevBtn = root.querySelector('[data-prev]');
            const nextBtn = root.querySelector('[data-next]');

            const items = [
                {alt: 'Telecom', src: '/assets/img/customers_telecom.jpg'},
                {alt: 'Military&Aerospace', src: '/assets/img/customers_military.jpg'},
                {alt: 'Automotive', src: '/assets/img/customers_auto.jpg'},
                {alt: 'Consumer', src: '/assets/img/customers_consumer.jpg'},
                {alt: 'Office', src: '/assets/img/customers_office.jpg'},
                {alt: 'Mobile', src: '/assets/img/customers_mobile.jpg'},
                {alt: 'Industrial', src: '/assets/img/customers_industrial.jpg'},
                {alt: 'Medical', src: '/assets/img/customers_medical.jpg'},
            ];

            let idx = 0;
            let timer = null;

            const setActiveTab = (i) => {
                tabs.forEach((t, k) => {
                    const on = k === i;
                    t.classList.toggle('is-on', on);
                    t.setAttribute('aria-selected', on ? 'true' : 'false');
                });
            };

            const fitStageHeight = () => {
                const h = img.getBoundingClientRect().height;
                if (h > 0) stage.style.height = `${Math.ceil(h)}px`;
            };

            const show = (i) => {
                idx = (i + items.length) % items.length;
                const item = items[idx];

                img.style.opacity = '0';
                img.style.transform = 'translateY(2px)';

                const swap = () => {
                    img.src = item.src;
                    img.alt = item.alt;
                    setActiveTab(idx);

                    img.onload = () => {
                        fitStageHeight();
                        requestAnimationFrame(() => {
                            img.style.opacity = '1';
                            img.style.transform = 'translateY(0)';
                        });
                    };
                };

                setTimeout(swap, 120);
            };

            const start = () => {
                stop();
                timer = setInterval(() => show(idx + 1), 3000);
            };
            const stop = () => {
                if (timer) clearInterval(timer);
                timer = null;
            };

            img.style.transition = 'opacity .22s ease, transform .22s ease';

            tabs.forEach((btn) => {
                btn.addEventListener('click', () => {
                    const n = Number(btn.dataset.tab || 0);
                    show(n);
                    start();
                });
            });

            prevBtn?.addEventListener('click', () => {
                show(idx - 1);
                start();
            });
            nextBtn?.addEventListener('click', () => {
                show(idx + 1);
                start();
            });

            const initFit = () => {
                fitStageHeight();
                start();
            };
            if (img.complete) initFit();
            else img.addEventListener('load', initFit);

            window.addEventListener('resize', () => fitStageHeight(), {passive: true});

            stage.addEventListener('mouseenter', stop);
            stage.addEventListener('mouseleave', start);

            show(0);
        })();
    </script>

    <script>
        (() => {
            const root = document.querySelector('[data-mv-slider]');
            if (!root) return;

            const track = root.querySelector('[data-mv-track]');
            const prevBtn = root.querySelector('[data-mv-prev]');
            const nextBtn = root.querySelector('[data-mv-next]');
            const dotsWrap = root.querySelector('[data-mv-dots]');
            if (!track) return;

            const originals = Array.from(track.querySelectorAll('.mv-slide'));
            const n = originals.length;
            if (n <= 1) return;

            let dots = [];
            if (dotsWrap) {
                dotsWrap.innerHTML = "";
                dots = Array.from({length: n}, (_, i) => {
                    const b = document.createElement('button');
                    b.type = "button";
                    b.className = "mv-dot";
                    b.setAttribute('aria-label', `슬라이드 ${i + 1}`);
                    b.dataset.dot = String(i);
                    b.addEventListener('click', () => {
                        scrollToIndex(n + i, 'smooth');
                        start();
                    });
                    dotsWrap.appendChild(b);
                    return b;
                });
            }

            const makeClones = () => originals.map(el => {
                const c = el.cloneNode(true);
                c.dataset.clone = "1";
                return c;
            });

            const pre = makeClones();
            const post = makeClones();
            track.prepend(...pre);
            track.append(...post);

            const slides = () => Array.from(track.querySelectorAll('.mv-slide'));

            let idx = n;
            let timer = null;
            let paused = false;

            const getSlideCenterOffset = (el) => {
                const trackRect = track.getBoundingClientRect();
                const elRect = el.getBoundingClientRect();
                const trackCenter = trackRect.left + trackRect.width / 2;
                const elCenter = elRect.left + elRect.width / 2;
                return (elCenter - trackCenter);
            };

            const updateDots = () => {
                if (!dots.length) return;
                const real = ((idx - n) % n + n) % n;
                dots.forEach((d, k) => d.classList.toggle('is-on', k === real));
            };

            const setActiveByNearestCenter = () => {
                const list = slides();
                const trackRect = track.getBoundingClientRect();
                const centerX = trackRect.left + trackRect.width / 2;

                let best = 0, bestDist = Infinity;
                list.forEach((s, i) => {
                    const r = s.getBoundingClientRect();
                    const c = r.left + r.width / 2;
                    const d = Math.abs(c - centerX);
                    if (d < bestDist) {
                        bestDist = d;
                        best = i;
                    }
                });

                list.forEach((s, i) => s.classList.toggle('is-active', i === best));
                idx = best;
                updateDots();
            };

            const fixLoop = () => {
                const list = slides();

                if (idx >= 2 * n) {
                    idx -= n;
                    const el = list[idx];
                    if (!el) return;
                    const offset = getSlideCenterOffset(el);
                    track.scrollBy({left: offset, top: 0, behavior: 'auto'});
                } else if (idx < n) {
                    idx += n;
                    const el = list[idx];
                    if (!el) return;
                    const offset = getSlideCenterOffset(el);
                    track.scrollBy({left: offset, top: 0, behavior: 'auto'});
                }
            };

            const scrollToIndex = (i, behavior = 'smooth') => {
                const list = slides();
                const el = list[i];
                if (!el) return;

                const offset = getSlideCenterOffset(el);
                track.scrollBy({left: offset, top: 0, behavior});

                idx = i;

                window.setTimeout(() => {
                    fixLoop();
                    setActiveByNearestCenter();
                }, behavior === 'smooth' ? 360 : 0);
            };

            const next = () => scrollToIndex(idx + 1, 'smooth');
            const prev = () => scrollToIndex(idx - 1, 'smooth');

            const start = () => {
                stop();
                timer = window.setInterval(() => {
                    if (!paused) next();
                }, 2300);
            };

            const stop = () => {
